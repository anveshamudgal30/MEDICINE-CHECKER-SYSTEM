def search_medicine(name):
    key = name.lower()
    if key in medicines:
        print("\n--- Medicine Details ---")
        for field, value in medicines[key].items():
            print(f"{field.capitalize()}: {value}")
    else:
        print("Medicine not found in the database.")

def add_medicine():
    name = input("Enter medicine name: ").lower()
    medicines[name] = {
        "uses": input("Enter uses: "),
        "dosage": input("Enter dosage: "),
        "side_effects": input("Enter side effects: "),
        "precautions": input("Enter precautions: "),
        "availability": input("Enter availability: ")
    }
    print("New medicine successfully added to the system.")

def main():
    while True:
        print("\nMenu Options: 1.Search  2.Add  3.Show All  4.Exit")
        choice = input("Choose an option: ")
        if choice == "1":
            search_medicine(input("Enter medicine name: "))
        elif choice == "2":
            add_medicine()
        elif choice == "3":
            print("\nList of Medicines:")
            for med in medicines:
                print("-", med.capitalize())
        elif choice == "4":
            print("Thank you for using the Medicine Checker System.")
            break
        else:
            print("Invalid choice. Please choose again.")

main()






































